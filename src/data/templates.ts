/**
 * 인기 품목만 필터링 (TOP 30)
 */
export function getPopularTemplates(): FoodTemplate[] {
  return FOOD_TEMPLATES.filter(t => t.is_popular).sort((a, b) => a.sort_order - b.sort_order);
}

/**
 * 카테고리별 템플릿 그룹화
 */
export function getTemplatesByCategory(): Map<string, FoodTemplate[]> {
  const grouped = new Map<string, FoodTemplate[]>();
  for (const t of FOOD_TEMPLATES) {
    const existing = grouped.get(t.category) ?? [];
    existing.push(t);
    grouped.set(t.category, existing);
  }
  return grouped;
}
